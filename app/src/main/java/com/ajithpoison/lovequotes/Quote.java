package com.ajithpoison.lovequotes;

class Quote {
    private String quote;
    private Integer quoteID;

    Quote() {}

    String getQuote() {
        return quote;
    }

    void setQuote(String quote) {
        this.quote = quote;
    }

    Integer getQuoteID() {
        return quoteID;
    }

    void setQuoteID(Integer quoteID) {
        this.quoteID = quoteID;
    }
}
